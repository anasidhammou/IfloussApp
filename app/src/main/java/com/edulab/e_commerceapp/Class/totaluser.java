package com.edulab.e_commerceapp.Class;

 public class totaluser {

  private String total;
  private String name;

  public totaluser(String total, String name) {
   this.total = total;
   this.name = name;
  }

  public String total() {
   return total;
  }

  public void setTotal(String total) {
   this.total = total;
  }

  public String name() {
   return name;
  }

  public void setName(String name) {
   this.name = name;
  }
 }
